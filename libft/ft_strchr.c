#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;

	p = NULL;
	while (*s != '\0')
	{
		if ((char)c == *s)
		{
			p = (char *)s;
			return (p);
		}
		s++;
	}
	if ((char)c == *s)
		p = (char *)s;
	return (p);
}
